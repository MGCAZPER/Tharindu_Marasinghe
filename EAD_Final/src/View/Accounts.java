    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String Username = txtUsename.getText();
        String Password = txtPassword.getText();
        String Role = cmbRole.getSelectedItem().toString();
        String EMP_ID = txtEmpID.getText();
        String Email = txtEEmail.getText();

        if (Username.equals("") || Password.equals("")|| Role.equals("")|| EMP_ID.equals("")|| Email.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fill all fields");
            return;
        }

        try {
            DBConnection dbcon =DBConnection.getInstance();
            Connection con=dbcon.GetConnection();
            con=DBConnection.getInstance().GetConnection();;

            String sql = "INSERT INTO login ( UserName, Password,Email,Emp_id,Role) VALUES (?, ?, ?,?,?)";

            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, Username);
            pst.setString(2, Password);
            pst.setString(3, Email);
            pst.setString(4, EMP_ID);
            pst.setString(5, Role);

            pst.executeUpdate();
            txtUsename.setText("");
            txtPassword.setText("");

            txtEmpID.setText("");
            txtEEmail.setText("");

            javax.swing.JOptionPane.showMessageDialog(this, "Saved");

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "Error Occured !");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

