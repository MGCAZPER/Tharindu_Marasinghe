    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    
        //Personal Details 
    String Fname = txtFname.getText();
    String Lname = txtLname.getText();
    String NIC = txtNIC.getText();
    String Adress = txtAdress.getText();
    String Email = txtEmail.getText();
    String DOB = txtDOB.getText();
    String Contact = txtContact.getText();
        //Educational Details
    String University = txtUni.getText();
    String Special = txtService.getText();
    String Service = txtSpec.getText();
    String Fac = cmbFac.getSelectedItem().toString();
    String Time = cmbTime .getSelectedItem().toString();
        

    if (Fname.equals("") || Lname.equals("")|| NIC.equals("")|| Adress.equals("")|| Email.equals("")|| DOB.equals("")
            || University.equals("") || Special.equals("")|| Service.equals("")|| Fac.equals("")|| Time.equals("")|| Contact.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fill all fields");
        return;
    }

    try {
                DBConnection dbcon =DBConnection.getInstance();
                Connection con=dbcon.GetConnection();
                con=DBConnection.getInstance().GetConnection();;

        String sql = "INSERT INTO lecturer ( Fname,Lname,NIC,Adress,Email,Time,Contact,Fac,Spec,Uni,Ser) VALUES (?, ?, ?,?,?,?,?,?,?,?,?)";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, Fname);
        pst.setString(2, Lname);
        pst.setString(3, NIC);
        pst.setString(4, Adress);
        pst.setString(5, Email);
        pst.setString(6, Time);
        pst.setString(7, Contact);
        pst.setString(8, Fac);
        pst.setString(9, Special);
        pst.setString(10, University);
        pst.setString(11, Service);

        pst.executeUpdate();
    //CLear Personal
    txtFname.getText();
    txtLname.getText();
    txtNIC.getText();
    txtAdress.getText();
    txtEmail.getText();
    txtDOB.getText();
    txtContact.getText();
        //Clear Educational Details
    txtUni.getText();
    txtService.getText();
    txtSpec.getText();
     

        javax.swing.JOptionPane.showMessageDialog(this, "Saved");
            } catch (Exception e) {
        System.out.println(e);
        JOptionPane.showMessageDialog(rootPane, "Error Occured !");
    }

    }//GEN-LAST:event_jButton6ActionPerformed

