    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    String Module = txtModule.getText();
    String Lecturer = txtLecturer.getText();
    String Location = txtLocation.getText();
    String Batch = txtBatch.getText();
    String Date = txtDate.getText();
    String Session = txtSession.getText();
    

    if (Module.equals("") || Lecturer.equals("")|| Location.equals("")|| Batch.equals("")|| Date.equals("")|| Session.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fill all fields");
        return;
    }

    try {
                DBConnection dbcon =DBConnection.getInstance();
                Connection con=dbcon.GetConnection();
                con=DBConnection.getInstance().GetConnection();;

        String sql = "INSERT INTO schedual ( Module, LectureName,Location,Batch,Session,Date) VALUES (?, ?, ?,?,?,?)";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, Module);
        pst.setString(2, Lecturer);
        pst.setString(3, Location);
        pst.setString(4, Batch);
        pst.setString(5, Session);
        pst.setString(6, Date);

        pst.executeUpdate();
        txtModule.setText("");
        txtLecturer.setText("");
        txtLocation.setText("");
        txtBatch.setText("");
        txtDate.setText("");
        txtSession.setText("");

        javax.swing.JOptionPane.showMessageDialog(this, "Saved");

    } catch (Exception e) {
        System.out.println(e);
        JOptionPane.showMessageDialog(rootPane, "Error Occured !");
    }        
    }//GEN-LAST:event_jButton6ActionPerformed

