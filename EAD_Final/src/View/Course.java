    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    String name = txtCName.getText();
    String AFee = txtAFee.getText();
    String CFee = txtCFee.getText();
    String CD = txtCD.getText();
    String Duration = txtDuration.getText();
    

    if (name.equals("") || AFee.equals("")|| CFee.equals("")|| CD.equals("")|| Duration.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fill all fields");
        return;
    }

    try {
                DBConnection dbcon =DBConnection.getInstance();
                Connection con=dbcon.GetConnection();
                con=DBConnection.getInstance().GetConnection();;

        String sql = "INSERT INTO course ( CName, Afee,CFee,CDirector,Duration) VALUES (?, ?, ?,?,?)";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, name);
        pst.setString(2, AFee);
        pst.setString(3, CFee);
        pst.setString(4, CD);
        pst.setString(5, Duration);

        pst.executeUpdate();
        txtCName.setText("");
        txtAFee.setText("");
        txtCFee.setText("");
        txtCD.setText("");
        txtDuration.setText("");

        javax.swing.JOptionPane.showMessageDialog(this, "Saved");

    } catch (Exception e) {
        System.out.println(e);
        JOptionPane.showMessageDialog(rootPane, "Error Occured !");
    }
    }//GEN-LAST:event_jButton6ActionPerformed

